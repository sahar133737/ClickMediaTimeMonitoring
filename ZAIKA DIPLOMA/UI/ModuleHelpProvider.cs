        public static void ShowHelp(string moduleKey, IWin32Window owner)
        {
            if (!HelpTexts.TryGetValue(moduleKey, out var text))
            {
                text = "Для этого раздела текст справки ещё не добавлен.";
            }

            var title = ModuleTitles.TryGetTitle(moduleKey);
            using (var f = new HelpFlyoutForm(title, text))
            {
                f.ShowDialog(owner);
            }
        }

        public static void BindF11(Form form, string moduleKey)
        {
            if (form == null)
            {
                return;
            }

            form.KeyPreview = true;
            form.KeyDown += (s, e) =>
            {
                if (e.KeyCode != Keys.F11)
                {
                    return;
                }

                e.Handled = true;
                e.SuppressKeyPress = true;
                ShowHelp(moduleKey, form);
            };
        }
    }

